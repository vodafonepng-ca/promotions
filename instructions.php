
<div class="container-fluid">
  <h2>VF-PNG Promotions Draw Portal</h2>
  <h3 class="text-primary">Instructions of use</h3>
  <p>The portal  does not have a preloaded data source, neither does it connects to a data source.
This means that upon the process of making the draws the user have to load data from an external source (via a csv file).
  </p>
   <h4> Preparing of  Data for Draw (Important)<sup class="text-danger">*</sup></h4>
   <ol>
     <li class="mb-3">Use  excel or any spread sheet application  to collect data needed to run the draws;
     Make sure to  have them in one  column; without column title; (remove any column title)<sup class="text-danger">*</sup>;</li>
  <li class="mb-3">Save them in a (.csv) file format and import them by clicking the <button type="button" class="btn btn-info" disabled>LOAD ENTRIES</button> button.</li>

  <li class="mb-3">Click the <button class="btn btn-success" disabled>GO</button> button to run the Draw</li>
  <li class="mb-3">Click the <button  class="btn btn-info" disabled>Save Winner</button> button below the draw result to save the result of the draw being made. Repeat the third  and fourth step for the number of draws you want to make.</li>
  <li class="mb-3">Click <button class="btn btn-info" disabled>SHOW RESULTS </button> button on the top to view the populated results of the draws being made.</li>
  <li class="mb-3">Click  <button type="button" class="btn btn-default" disabled>Download Results</button> button from the popup dialogue to download the results in a .csv format  file.</li>
  <li class="mb-3">Click the  <button class="btn btn-danger" disabled>RESET</button> button on the top left to reset all the Data being uploaded or Populated within the Draw portal.</li>
   </ol>

   <p>Note:
   You will have to reupload the data file and restart the whole process after resetting.</p>
</div>
